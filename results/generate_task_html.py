def find_repo_root(start_path: Path) -> Path:
    """Find the repository root by looking for .git directory."""
    current = start_path.resolve()
    while current != current.parent:
        if (current / ".git").exists():
            return current
        current = current.parent
    return start_path.resolve()


def trim_binary_diffs(patch_content: str) -> str:
    """
    Remove binary file diffs from patch content to reduce size.
    Binary diffs are identified by 'Binary files ... differ' or GIT binary patch markers.
    """
    lines = patch_content.split("\n")
    result = []
    skip_until_next_diff = False
    i = 0

    while i < len(lines):
        line = lines[i]

        # Check for start of a new diff
        if line.startswith("diff --git"):
            skip_until_next_diff = False
            # Look ahead to see if this is a binary file
            j = i + 1
            while j < len(lines) and not lines[j].startswith("diff --git"):
                if "Binary files" in lines[j] or "GIT binary patch" in lines[j]:
                    skip_until_next_diff = True
                    result.append(line)
                    result.append("[Binary file diff removed]")
                    break
                j += 1

            if skip_until_next_diff:
                # Skip to next diff
                i += 1
                while i < len(lines) and not lines[i].startswith("diff --git"):
                    i += 1
                continue

        if not skip_until_next_diff:
            result.append(line)
        i += 1

    return "\n".join(result)


def load_file_content(file_path: Path) -> str | None:
    """Load a file and return its content."""
    if not file_path.exists():
        return None
    try:
        return file_path.read_text(encoding="utf-8", errors="replace")
    except Exception as e:
        return f"[Error loading file: {e}]"


def load_patch(patch_path: Path, trim_binary: bool = True) -> str | None:
    """Load a patch file, optionally trimming binary diffs."""
    content = load_file_content(patch_path)
    if content and trim_binary:
        return trim_binary_diffs(content)
    return content


def calculate_loc_from_patch(patch_content: str | None) -> dict[str, int]:
    """Calculate lines of code added and deleted from patch content."""
    additions = 0
    deletions = 0
    if not patch_content:
        return {"code_additions": 0, "code_deletions": 0}

    for line in patch_content.splitlines():
        if line.startswith("+++") or line.startswith("---"):
            continue
        if line.startswith("+"):
            additions += 1
        elif line.startswith("-"):
            deletions += 1

    return {"code_additions": additions, "code_deletions": deletions}


def load_task_yaml(tasks_dir: Path, instance_id: str) -> dict[str, Any] | None:
    """Load task.yaml for a given instance ID."""
    task_path = tasks_dir / instance_id / "task.yaml"
    if not task_path.exists():
        return None
    try:
        with open(task_path) as f:
            return yaml.safe_load(f)
    except Exception as e:
        return {"error": str(e)}


def load_scores(run_path: Path) -> dict[str, Any]:
    """Load scores from scores.json or sharded score files."""
    scores_path = run_path / "scores.json"
    if scores_path.exists():
        try:
            with open(scores_path) as f:
                return json.load(f)
        except Exception as e:
            return {"error": f"Error loading {scores_path}: {e}"}

    # If scores.json not found, look for sharded files
    all_scores = {}
    score_files = list(run_path.glob("[0-9]*_to_[0-9]*_scores.json"))
    if not score_files:
        return {}  # No scores files found at all

    for score_file in score_files:
        try:
            with open(score_file) as f:
                data = json.load(f)
                all_scores.update(data)
        except Exception as e:
            print(f"Error loading score file {score_file}: {e}", file=sys.stderr)

    if not all_scores and score_files:
        return {"error": "All sharded score files failed to load."}

    return all_scores


def load_properties(filepath: Path, sep="=", comment_char="#") -> dict[str, str]:
    """
    Read the file passed as parameter as a properties file.
    """
    props: dict[str, str] = {}
    if not filepath.exists():
        return {}

    with open(filepath, "rt") as f:
        for line in f:
            l = line.strip()
            if l and not l.startswith(comment_char):
                key_value = l.split(sep)
                key = key_value[0].strip()
                value = sep.join(key_value[1:]).strip().strip('"')
                props[key] = value
    return props


def load_reviews_data(
    csv_path: Path,
) -> tuple[dict[str, dict[str, str]], list[str]]:
    """Load reviews data from a CSV file."""
    reviews_data = {}
    headers = []
    if not csv_path.exists():
        return {}, []
    try:
        with open(csv_path, "r", newline="", encoding="utf-8") as f:
            reader = csv.DictReader(f)
            headers = (
                [h for h in reader.fieldnames if h != "instance_id"]
                if reader.fieldnames
                else []
            )
            for row in reader:
                if "instance_id" in row:
                    instance_id = row.pop("instance_id")
                    reviews_data[instance_id] = row
    except Exception as e:
        print(f"Error loading reviews CSV {csv_path}: {e}", file=sys.stderr)
    return reviews_data, headers


def extract_model_name(folder: Path | str) -> str:
    """Extract model name from folder name like 'anthropic-claude-sonnet-4-5_2025-12-01-22-32-26'."""
    folder = Path(folder)
    # We try locating config.properties and get the model_name from there
    config = folder / CONFIG_PROPERTIES_FILE
    props = load_properties(config)
    props_model_name = props.get("model_name")
    if props_model_name:
        return props_model_name.replace("/", "-")

    # Otherwise just use the folder
    folder_name = folder.name

    # Remove timestamp suffix
    parts = folder_name.rsplit("_", 1)
    if len(parts) == 2 and re.match(r"\d{4}-\d{2}-\d{2}", parts[1]):
        return parts[0]
    return folder_name


def discover_model_runs(input_dir: Path) -> list[ModelRun]:
    """Discover all model run folders in the input directory.

    When multiple runs exist for the same model, each run is assigned a
    sequential run number and a display_name in the format 'model_name (run N)'.
    If only one run exists for a model, the display_name is just the model_name.
    """
    # First pass: collect basic run info
    preliminary_runs: list[tuple[str, Path, str]] = []  # (folder, path, model_name)
    for item in input_dir.iterdir():
        if item.is_dir():
            # If the item has only one subdirectory, use it as the item
            # BUT only if it doesn't already look like a run folder
            has_scores_json = (item / "scores.json").exists()
            has_sharded_scores = any(item.glob("[0-9]*_to_[0-9]*_scores.json"))
            if not (has_scores_json or has_sharded_scores):
                subdirs = [d for d in item.iterdir() if d.is_dir()]
                if len(subdirs) == 1:
                    item = subdirs[0]

            has_scores_json = (item / "scores.json").exists()
            has_sharded_scores = any(item.glob("[0-9]*_to_[0-9]*_scores.json"))
            # For tests, we might want to discover folders even without scores if they have patches
            has_patches = (item / "patches").exists()
            if has_scores_json or has_sharded_scores or has_patches:
                preliminary_runs.append((item.name, item, extract_model_name(item)))

    # Sort by model_name first, then by folder name (which includes timestamp)
    preliminary_runs.sort(key=lambda x: (x[2], x[0]))

    # Count runs per model
    model_counts: dict[str, int] = {}
    for _, _, model_name in preliminary_runs:
        model_counts[model_name] = model_counts.get(model_name, 0) + 1

    # Build final ModelRun objects with run numbers and display names
    runs: list[ModelRun] = []
    model_run_indices: dict[str, int] = {}
    for folder, path, model_name in preliminary_runs:
        model_run_indices[model_name] = model_run_indices.get(model_name, 0) + 1
        run_number = model_run_indices[model_name]

        if model_counts[model_name] > 1:
            display_name = f"{model_name} (run {run_number})"
        else:
            display_name = model_name

        runs.append(
            ModelRun(
                folder=folder,
                path=path,
                model_name=model_name,
                display_name=display_name,
                run_number=run_number,
            )
        )

    return runs


def collect_all_instance_ids(model_runs: list[ModelRun]) -> set[str]:
    """Collect all unique instance IDs from verifier folder name across all model runs."""
    instance_ids = set()
    for run in model_runs:
        combined_results_path = run.path / "combined_results.json"
        verifier_dir = run.path / "verifier"
        patches_dir = run.path / "patches"

        # Try combined_results first (should contain all the ran tasks)
        if combined_results_path.exists():
            with open(combined_results_path) as f:
                data = json.load(f)

                for attr, value in data.items():
                    instance_ids.add(attr)
        # Try the verifier folder otherwise
        elif verifier_dir.exists():
            for task_folder_id in verifier_dir.iterdir():
                instance_ids.add(task_folder_id.name)

        # Try the patches dir as last resort (doesn't work for oracle-agent as it has all the patches)
        elif patches_dir.exists():
            for patch_file in patches_dir.glob("*.patch"):
                # Extract instance ID from filename (remove .patch extension)
                instance_id = patch_file.stem
                instance_ids.add(instance_id)

    return instance_ids


def _messages_to_steps(messages: list[dict]) -> list[list[dict]]:
    """Group messages into "pages" as shown by the UI."""
    steps = []
    current_step = []
    for message in messages:
        current_step.append(message)
        if message["role"] == "user":
            steps.append(current_step)
            current_step = []
    if current_step:
        steps.append(current_step)
    return steps


def load_trajectory(trajectory_path: Path) -> dict[str, Any] | None:
    """Load trajectory JSON and extract messages (excluding system messages)."""
    if not trajectory_path.exists():
        return None
    try:
        with open(trajectory_path) as f:
            data = json.load(f)

        messages = data.get("messages", [])
        # Filter out system messages and extract role + content
        filtered_messages = []
        for msg in messages:
            role = msg.get("role")
            if role == "system":
                continue
            content = msg.get("content", "")
            # Content can be a string or an array of objects
            if isinstance(content, list):
                # Extract text from content array
                text_parts = []
                for part in content:
                    if isinstance(part, dict) and part.get("type") == "text":
                        text_parts.append(part.get("text", ""))
                    elif isinstance(part, str):
                        text_parts.append(part)
                content = "\n".join(text_parts)
            filtered_messages.append({"role": role, "content": content})

        steps = _messages_to_steps(filtered_messages)
        step_limit = (
            data.get("info", {}).get("config", {}).get("agent", {}).get("step_limit")
        )
        cost = data.get("info", {}).get("model_stats", {}).get("instance_cost")
        cost_limit = (
            data.get("info", {}).get("config", {}).get("agent", {}).get("cost_limit")
        )

        # Extract bash commands from messages
        bash_commands = []
        # Regex to match bash code blocks. Uses re.DOTALL to match across multiple lines
        bash_regex = re.compile(r"```bash\n(.*?)\n?```", re.DOTALL)
        for i, msg in enumerate(filtered_messages):
            # Skip the prompt and first system message
            if i < 1:
                continue

            content = msg.get("content", "")
            for match in bash_regex.finditer(content):
                if match.group(1):
                    bash_commands.append(
                        {"command": match.group(1).strip(), "message_index": i}
                    )

        return {
            "messages": filtered_messages,
            "step_count": len(steps),
            "step_limit": step_limit,
            "cost": cost,
            "cost_limit": cost_limit,
            "exit_status": data.get("info", {}).get("exit_status"),
            "submission": (
                (
                    data.get("info", {}).get("submission")[:100] + "..."
                    if isinstance(data.get("info", {}).get("submission"), str)
                    and data.get("info", {})
                    .get("submission", "")
                    .startswith("diff --git")
                    and len(data.get("info", {}).get("submission")) > 100
                    else data.get("info", {}).get("submission")
                )
            ),
            "bash_commands": bash_commands,
        }
    except Exception as e:
        return {
            "messages": [{"role": "error", "content": f"Error loading trajectory: {e}"}]
        }


def generate_task_data(
    instance_id: str,
    model_runs: list[ModelRun],
    tasks_dir: Path,
    reviews_results: list[dict[str, Any]] | None = None,
) -> dict[str, Any]:
    """Generate all data needed for a single task HTML."""
    # Load task definition
    task_yaml = load_task_yaml(tasks_dir, instance_id)
    task_bq_data = None

    # Process multiple reviews files for this instance
    task_reviews_results = []
    if reviews_results:
        for result in reviews_results:
            filename = result["filename"]
            all_data = result["data"]
            if instance_id in all_data:
                task_reviews_results.append(
                    {"filename": filename, "data": all_data[instance_id]}
                )

    issue_url = None
    if task_yaml and "issues" in task_yaml and task_yaml["issues"]:
        first_issue = task_yaml["issues"][0]
        if "url" in first_issue:
            issue_url = first_issue["url"]
        elif (
            "id" in first_issue
            and "repository" in task_yaml
            and "url" in task_yaml["repository"]
        ):
            repo_url = task_yaml["repository"]["url"]
            issue_id = first_issue["id"]
            # Construct GitHub issue URL from repository URL and issue ID
            issue_url = f"{repo_url}/issues/{issue_id}"

    if issue_url and not (
        issue_url.startswith("http://") or issue_url.startswith("https://")
    ):
        issue_url = f"http://{issue_url}"

    # Load golden patch
    golden_patch = load_patch(
        tasks_dir / instance_id / "golden.patch", trim_binary=True
    )
    test_patch = load_patch(tasks_dir / instance_id / "test.patch", trim_binary=True)
    validate_script = load_file_content(tasks_dir / instance_id / "validate.sh")

    # Collect results from each model
    model_results = []
    for run in model_runs:
        scores = load_scores(run.path)
        result = scores.get(instance_id)

        # Load patch for this model
        patch_path = run.path / "patches" / f"{instance_id}.patch"
        patch_content = load_patch(patch_path, trim_binary=True)

        # Load trajectory for this model
        trajectory_path = run.path / "trajectories" / f"{instance_id}.json"
        trajectory = load_trajectory(trajectory_path)

        # Load verifier log
        verifier_log_path = run.path / "verifier" / instance_id / "log.txt"
        verifier_log = None
        if verifier_log_path.exists():
            try:
                verifier_log = verifier_log_path.read_text(
                    encoding="utf-8", errors="replace"
                )
            except Exception as e:
                verifier_log = f"[Error loading verifier log: {e}]"

        model_results.append(
            {
                "model_name": run.model_name,
                "display_name": run.display_name,
                "folder": run.folder,
                "result": result,
                "patch": patch_content,
                "trajectory": trajectory,
                "verifier_log": verifier_log,
            }
        )

    if golden_patch:
        task_bq_data = calculate_loc_from_patch(golden_patch)
    elif model_results:
        first_patch = next((r["patch"] for r in model_results if r["patch"]), None)
        task_bq_data = calculate_loc_from_patch(first_patch)
    else:
        task_bq_data = {"code_additions": 0, "code_deletions": 0}

    return {
        "instance_id": instance_id,
        "task": task_yaml,
        "issue_url": issue_url,
        "golden_patch": golden_patch,
        "test_patch": test_patch,
        "validate_script": validate_script,
        "model_results": model_results,
        "bq_data": task_bq_data,
        "reviews_results": task_reviews_results,
    }


def escape_for_script_tag(json_str: str) -> str:
    """Escape JSON string for safe embedding in a <script> tag."""
    # Prevent closing script tag from being interpreted
    json_str = json_str.replace("</script>", "<\\/script>")
    json_str = json_str.replace("</Script>", "<\\/Script>")
    json_str = json_str.replace("</SCRIPT>", "<\\/SCRIPT>")
    return json_str


