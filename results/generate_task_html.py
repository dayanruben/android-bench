def main():
    args = parse_args()

    input_dir = Path(args.input_dir).resolve()
    if not input_dir.exists():
        print(f"Error: Input directory does not exist: {input_dir}", file=sys.stderr)
        sys.exit(1)

    repo_root = ROOT_DIR  # find_repo_root(input_dir)

    # Set default paths
    output_dir = Path(args.output_dir) if args.output_dir else input_dir / "summary"
    tasks_dir = Path(args.tasks_dir) if args.tasks_dir else repo_root / "dataset/tasks"

    print(f"Input directory: {input_dir}")
    print(f"Output directory: {output_dir}")
    print(f"Tasks directory: {tasks_dir}")

    # Discover model runs
    model_runs = discover_model_runs(input_dir)
    if not model_runs:
        print("Error: No model runs found in input directory", file=sys.stderr)
        sys.exit(1)

    print(f"Found {len(model_runs)} model runs:")
    [print(r.display_name) for r in model_runs]

    # Collect instance IDs
    if args.instance_id:
        instance_ids = {args.instance_id}
    else:
        instance_ids = collect_all_instance_ids(model_runs)

    if args.tasks_filter:
        filter_tasks_list = []
        negate_filter = False
        if args.tasks_filter.startswith("!"):
            negate_filter = True
            tasks_filter_path = Path(args.tasks_filter[1:])
        else:
            tasks_filter_path = Path(args.tasks_filter)

        if tasks_filter_path.exists():
            with open(tasks_filter_path, "r") as f:
                filter_tasks_list = yaml.safe_load(f)

            if negate_filter:
                instance_ids = {
                    id for id in instance_ids if id not in filter_tasks_list
                }
            else:
                instance_ids = {id for id in instance_ids if id in filter_tasks_list}
        else:
            print(f"Warning: filter file {tasks_filter_path} not found.")

    print(f"Processing {len(instance_ids)} tasks")

    # Create output directory
    output_dir.mkdir(parents=True, exist_ok=True)

    # Build list of display names for CSV header (unique per run)
    display_names = [run.display_name for run in model_runs]
    # Group runs by run number for per-run CSVs
    runs_by_number: dict[int, list[ModelRun]] = {}
    for run in model_runs:
        runs_by_number.setdefault(run.run_number, []).append(run)

    # Load multiple reviews files
    reviews_results = []
    all_reviews_headers = []
    if args.reviews:
        for path_str in args.reviews:
            p = Path(path_str)
            data, headers = load_reviews_data(p)
            reviews_results.append(
                {"filename": p.name, "data": data, "headers": headers}
            )
            for h in headers:
                if h not in all_reviews_headers:
                    all_reviews_headers.append(h)

    # Generate HTML for each task and collect data for CSV
    csv_rows = []
    csv_rows_by_run: dict[int, list[dict[str, Any]]] = {
        n: [] for n in runs_by_number.keys()
    }

    input_dir_name = input_dir.name
    for instance_id in sorted(instance_ids):
        print(f"  Generating: {instance_id}")
        task_data = generate_task_data(
            instance_id, model_runs, tasks_dir, reviews_results
        )
        html = generate_html(task_data, input_dir_name=input_dir_name)

        # Debugging large files
        html_size = len(html)
        if html_size > 30 * 1024 * 1024:  # > 30MB
            print(f"    Warning: Large HTML generated: {format_size(html_size)}")
            # Print breakdown of top-level keys in task_data
            for key, value in task_data.items():
                val_size = len(json.dumps(value))
                if val_size > 500 * 1024:  # > 500KB
                    print(f"      - {key}: {format_size(val_size)}")
                    if key == "model_results":
                        for i, res in enumerate(value):
                            res_size = len(json.dumps(res))
                            if res_size > 500 * 1024:
                                print(
                                    f"        - {res.get('display_name', i)}: {format_size(res_size)}"
                                )
                                for subkey, subval in res.items():
                                    subval_size = len(json.dumps(subval))
                                    if subval_size > 100 * 1024:
                                        print(
                                            f"          - {subkey}: {format_size(subval_size)}"
                                        )

        output_path = output_dir / f"{instance_id}.html"
        output_path.write_text(html, encoding="utf-8")

        # Collect row data for CSV
        row = {
            "instance_id": instance_id,
            "summary_file_name": f"{instance_id}.html",
            "category_ids": (
                task_data["task"].get("category_ids", []) if task_data["task"] else []
            ),
            "submission_type": (
                task_data["task"].get("submission_type", "N/A")
                if task_data["task"]
                else "N/A"
            ),
        }

        # Get time_estimate from task.yaml
        time_estimate = (
            task_data["task"].get("time_estimate", "N/A")
            if task_data["task"]
            else "N/A"
        )
        row["time_estimate"] = time_estimate

        # Add other metrics from reviews files to the row for CSV export
        for result in task_data.get("reviews_results", []):
            d = result["data"]
            for k, v in d.items():
                if k not in row:
                    row[k] = v

        if task_data.get("bq_data"):
            row["loc"] = (
                task_data["bq_data"]["code_additions"]
                + task_data["bq_data"]["code_deletions"]
            )

        for model_result in task_data["model_results"]:
            display_name = model_result["display_name"]
            result = model_result["result"]
            status = result["status"] if result else "NO_RESULT"
            row[display_name] = status
        csv_rows.append(row)

        # Populate per-run rows
        for run_number, runs_in_batch in runs_by_number.items():
            run_row = {
                "instance_id": instance_id,
                "summary_file_name": f"{instance_id}.html",
                "category_ids": row["category_ids"],
                "submission_type": row["submission_type"],
                "loc": row.get("loc", "N/A"),
                "time_estimate": row.get("time_estimate", "N/A"),
            }
            # Add all reviews data to per-run rows too for CSV export
            for header in all_reviews_headers:
                run_row[header] = row.get(header, "N/A")

            for run in runs_in_batch:
                # Find the result for this specific run
                # model_results order matches model_runs order
                run_idx = model_runs.index(run)
                model_result = task_data["model_results"][run_idx]

                model_name = run.model_name
                result = model_result["result"]
                status = result["status"] if result else "NO_RESULT"
                run_row[model_name] = status
            csv_rows_by_run[run_number].append(run_row)

    # Write index.csv
    csv_path = output_dir / "index.csv"
    with open(csv_path, "w", newline="", encoding="utf-8") as f:
        fieldnames = [
            "instance_id",
            "summary_file_name",
            "category_ids",
            "submission_type",
            "time_estimate",
        ]
        # Include other reviews headers if they exist and are not time_estimate
        for h in all_reviews_headers:
            if h != "time_estimate" and h not in fieldnames:
                fieldnames.append(h)
        fieldnames.append("loc")
        for name in display_names:
            fieldnames.append(name)
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(csv_rows)

    # Write per-run CSVs
    for run_number, runs_in_batch in runs_by_number.items():
        csv_path = output_dir / f"index_run{run_number}.csv"
        with open(csv_path, "w", newline="", encoding="utf-8") as f:
            fieldnames = [
                "instance_id",
                "summary_file_name",
                "category_ids",
                "submission_type",
                "time_estimate",
            ]
            for h in all_reviews_headers:
                if h != "time_estimate" and h not in fieldnames:
                    fieldnames.append(h)
            fieldnames.append("loc")
            for run in runs_in_batch:
                name = run.model_name
                fieldnames.append(name)
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(csv_rows_by_run[run_number])
        print(
            f"Generated index_run{run_number}.csv with {len(csv_rows_by_run[run_number])} entries"
        )

    # Generate index.html
    generate_index_html(output_dir, csv_rows, model_runs, input_dir_name=input_dir_name)

    print(f"\nGenerated {len(instance_ids)} HTML files in {output_dir}")
    print(f"Generated index.csv with {len(csv_rows)} entries")
    print("Generated index.html")

if __name__ == "__main__":
    main()
