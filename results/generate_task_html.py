"""
Generate self-contained HTML visualization files for Android benchmark tasks.

Each HTML file contains embedded JSON data for a single task, showing:
- Task details (instance ID, description, PR link)
- Results from each model run
- Patches generated by each model
- Golden patch (if available)

Usage:

    $ python scripts/generate_task_html.py --input-dir out-12-02
    $ python scripts/generate_task_html.py --input-dir out-12-02 --instance-id airbnb__lottie-android-pr_2064

Expected input directory structure:

    <input-dir>/
    ├── <model-run-1>/                    # e.g., anthropic-claude-sonnet-4-5_2025-12-01-22-32-26
    │   ├── scores.json                   # Results for each task: {instance_id: {status, score, ...}}
    │   ├── patches/
    │   │   ├── <instance-id>.patch       # Generated patch files
    │   │   └── ...
    │   └── trajectories/                 # (optional, not currently used)
    │       └── ...
    ├── <model-run-2>/
    │   └── ...
    └── ...

The script also expects:
- tasks/<instance-id>/task.yaml files in the repo root (for task descriptions)
- Golden patches in tests/harness_evaluation_integration_test/data/out/oracle-agent/patches/

Output:
- One HTML file per task in <output-dir>/ (default: <input-dir>/summary/)
- index.csv with instance_id, summary_file_name, and status per model
"""

